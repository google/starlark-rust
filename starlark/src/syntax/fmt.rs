//! AST printing utilities.

use std::fmt;

const TAB: &str = "  ";

/// Increment indent
pub(crate) fn indent(indent: &str) -> String {
    format!("{}{}", TAB, indent)
}

pub(crate) fn fmt_string_literal(f: &mut dyn fmt::Write, s: &str) -> fmt::Result {
    f.write_str("\"")?;
    for c in s.chars() {
        match c {
            '\n' => f.write_str("\\n")?,
            '\t' => f.write_str("\\t")?,
            '\r' => f.write_str("\\r")?,
            '\0' => f.write_str("\\0")?,
            '"' => f.write_str("\\\"")?,
            '\\' => f.write_str("\\\\")?,
            x => f.write_str(&x.to_string())?,
        }
    }
    f.write_str("\"")
}

pub(crate) fn comma_separated_fmt<I, F>(
    f: &mut dyn fmt::Write,
    v: &[I],
    converter: F,
    for_tuple: bool,
) -> fmt::Result
where
    F: Fn(&I, &mut dyn fmt::Write) -> fmt::Result,
{
    for (i, e) in v.iter().enumerate() {
        f.write_str(if i == 0 { "" } else { ", " })?;
        converter(e, f)?;
    }
    if v.len() == 1 && for_tuple {
        f.write_str(",")?;
    }
    Ok(())
}
